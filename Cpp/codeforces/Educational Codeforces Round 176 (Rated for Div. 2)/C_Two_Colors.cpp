using namespace std;

using u32 = unsigned;
using i64 = int64_t;
using u64 = uint64_t;

const char &ln = '\n';

void solve() {
    i64 n, m;
    cin >> n >> m;

    vector<i64> a(m);
    vector<i64> suffix_sum(n + 10);

    for (auto &x : a) {
        cin >> x;
        suffix_sum[x]++;
    }

    for (i64 i = n + 1; i >= 0; i--) {
        suffix_sum[i] += suffix_sum[i + 1];
    }

    i64 ans = 0;

    for (int i = 1; i <= n - 1; i++) {
        ans += suffix_sum[i] * suffix_sum[n - i] -
               min(suffix_sum[i], suffix_sum[n - i]);
    }

    cout << ans << ln;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;

    while (t--) {
        solve();
    }
    // fin.close();
    // fout.close();
    return 0;
}
